export const seedSaints2 = mutation({
  args: {},
  handler: async (ctx) => {
    const existing = await ctx.db.query("saints").collect();
    const existingKeys = new Set(
      existing.map((s) => `${s.month}-${s.day}-${s.name.toLowerCase()}`)
    );

    let added = 0;
    for (const s of ADDITIONAL_SAINTS) {
      const key = `${s.month}-${s.day}-${s.name.toLowerCase()}`;
      if (existingKeys.has(key)) continue;

      const doy = dayOfYear(s.month, s.day);
      const fullBio = buildBio(s);

      await ctx.db.insert("saints", {
        name: s.name,
        month: s.month,
        day: s.day,
        dayOfYear: doy,
        feastDate: s.feastDate,
        shortBio: s.shortBio,
        fullBio,
        bornYear: s.bornYear || undefined,
        diedYear: s.diedYear || undefined,
        patronOf: s.patronOf || undefined,
        feastType: s.feastType || undefined,
        liturgicalColor: s.liturgicalColor || undefined,
        canonizedBy: s.canonizedBy || undefined,
        tags: s.tags,
        isActive: true,
      });
      added++;
    }

    return { added };
  },
});
