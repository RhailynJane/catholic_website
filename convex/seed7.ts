function dayOfYear(month: number, day: number): number {
  const months = [0, 31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31];
  let doy = day;
  for (let i = 1; i < month; i++) doy += months[i];
  return doy;
}

function buildBio(s: {
  name: string;
  feastDate: string;
  shortBio: string;
  patronOf?: string;
  bornYear?: string;
  diedYear?: string;
  tags: string[];
  feastType?: string;
  liturgicalColor?: string;
  canonizedBy?: string;
}): string {
  return `<p>${s.shortBio}</p>
<p>${s.name} is venerated on ${s.feastDate} in the Roman Liturgical Calendar${s.feastType ? ` as a ${s.feastType}` : ""}. ${s.liturgicalColor ? `The liturgical color for this feast is ${s.liturgicalColor}.` : ""}</p>
${s.patronOf ? `<p><strong>Patronage:</strong> ${s.patronOf}</p>` : ""}
${s.tags.length ? `<p><strong>Known for:</strong> ${s.tags.join(", ")}.</p>` : ""}
${s.canonizedBy ? `<p><strong>Canonized by:</strong> ${s.canonizedBy}</p>` : ""}`;
}

